#include <iostream>
using namespace std;

int main(){
    int n, m,conMult=0;
    cout << "Ingresa M: ";
    cin >> m;
    cout<< "Ingresa N: ";
    cin >> n;
    for(int i=1;i*m<n;i++){
        cout << i*m<<endl;
        conMult++;
    }
    cout <<"Hay "<<conMult<<" multiplo\\s de "<< m<<" entre 1 y "<<n<<endl;
    return 0;
}